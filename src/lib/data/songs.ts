import type { SongMeta, Note, Measure, Rest } from "@/types/music";

function n(
  name: Note["name"],
  octave: Note["octave"],
  duration: Note["duration"],
  fingering: Note["fingering"] = null,
  accidental: Note["accidental"] = null,
  dotted = false
): Note {
  return { name, octave, duration, accidental, fingering, dotted, tied: false };
}

function m(...elements: (Note | Rest)[]): Measure {
  return { elements };
}

function r(duration: Note["duration"], dotted = false): Rest {
  return { duration, dotted };
}

